def _safe_filename(name: str) -> str:
    """Convert feature name to PascalCase filename."""
    import re
    clean = re.sub(r'[^a-zA-Z0-9\s-]', '', name)
    words = clean.replace('-', ' ').split()
    return ''.join(w.capitalize() for w in words) if words else 'Feature'


