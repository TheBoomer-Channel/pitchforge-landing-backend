"""Frontend codegen — core features → React pages + components."""

import logging
from pathlib import Path

from ..models import FunctionalSpec, TechnicalSpec

logger = logging.getLogger(__name__)


def generate_frontend(func: FunctionalSpec, tech: TechnicalSpec, output_dir: str) -> list[str]:
    """Generate React pages from core features spec.
    
    CodeGen 2.0: Includes i18n, themeSwitch, 10K quality components,
    lazy loading, and Vitest tests.
    """
    out = Path(output_dir)
    generated = []

    features = func.core_features or []
    pages_dir = out / "src/pages"
    comps_dir = out / "src/components"
    tests_dir = out / "src/__tests__"
    tests_dir.mkdir(parents=True, exist_ok=True)

    # ── 10K Components ──
    comps_to_generate = [
        ("ThemeToggle.tsx", _theme_toggle()),
        ("LanguageSwitcher.tsx", _language_switcher()),
        ("Card.tsx", _card_component()),
        ("Modal.tsx", _modal_component()),
        ("Table.tsx", _table_component()),
        ("Toast.tsx", _toast_component()),
        ("Button.tsx", _button_component()),
        ("Input.tsx", _input_component()),
        ("Skeleton.tsx", _skeleton_component()),
        ("EmptyState.tsx", _empty_state_component()),
        ("ErrorBoundary.tsx", _error_boundary()),
    ]
    for fname, content in comps_to_generate:
        p = comps_dir / fname
        p.write_text(content)
        generated.append(str(p))

    # ── Home page ──
    home = _home_page(features)
    p = pages_dir / "Home.tsx"
    p.write_text(home)
    generated.append(str(p))

    # ── Dashboard page ──
    dash = _dashboard_page(features)
    p = pages_dir / "Dashboard.tsx"
    p.write_text(dash)
    generated.append(str(p))

    # ── Feature-specific pages ──
    feature_names = []
    for f in features:
        fid = f.get("id", "")
        name = f.get("name", "")
        fname = _safe_filename(name or fid)
        feature_names.append(fname)
        page = _feature_page(f)
        p = pages_dir / f"{fname}.tsx"
        p.write_text(page)
        generated.append(str(p))

    # ── API hooks ──
    hooks = _api_hooks(features)
    p = out / "src/api/hooks.ts"
    p.write_text(hooks)
    generated.append(str(p))

    # ── Vitest tests ──
    vitest_tests = _generate_vitest_tests(feature_names)
    p = tests_dir / "components.test.tsx"
    p.write_text(vitest_tests)
    generated.append(str(p))

    logger.info(f"  ✦ Generated {len(generated)} frontend files")

    return generated


