def _api_hooks(features: list[dict]) -> str:
    return """import { useQuery, useMutation, useQueryClient } from '@tanstack/react-query'
import api from './client'

// Generic CRUD hooks — use these in your pages:
//
//   const { data, isLoading } = useList('users')
//   const { data } = useGet('users', 1)
//   const createMutation = useCreate('users')
//   const updateMutation = useUpdate('users')
//   const deleteMutation = useDelete('users')

export function useList(resource: string) {
  return useQuery({
    queryKey: [resource],
    queryFn: () => api.get(`/${resource}`).then(r => r.data),
  })
}

export function useGet(resource: string, id: number) {
  return useQuery({
    queryKey: [resource, id],
    queryFn: () => api.get(`/${resource}/${id}`).then(r => r.data),
  })
}

export function useCreate(resource: string) {
  const qc = useQueryClient()
  return useMutation({
    mutationFn: (data: unknown) => api.post(`/${resource}`, data),
    onSuccess: () => qc.invalidateQueries({ queryKey: [resource] }),
  })
}

export function useUpdate(resource: string) {
  const qc = useQueryClient()
  return useMutation({
    mutationFn: ({ id, data }: { id: number; data: unknown }) =>
      api.patch(`/${resource}/${id}`, data),
    onSuccess: () => qc.invalidateQueries({ queryKey: [resource] }),
  })
}

export function useDelete(resource: string) {
  const qc = useQueryClient()
  return useMutation({
    mutationFn: (id: number) => api.delete(`/${resource}/${id}`),
    onSuccess: () => qc.invalidateQueries({ queryKey: [resource] }),
  })
}
"""


